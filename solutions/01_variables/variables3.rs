#![allow(clippy::needless_late_init)]

fn main() {
    // Читання неініціалізованих змінних (uninitialized variables) не дозволено в Rust!
    // Тому спочатку потрібно присвоїти значення.
    let x: i32 = 42;

    println!("Число {x}");

    // Можна оголосити змінну і ініціалізувати її пізніше.
    // Але вона не може використовуватися до ініціалізації.
    let y: i32;
    y = 42;
    println!("Число {y}");
}
